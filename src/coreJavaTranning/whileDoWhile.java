package coreJavaTranning;

public class whileDoWhile {

	public static void main(String[] args) {
	
		int c=1;
		while(c<11)
		{
			System.out.println("The cvalues of J = "+c);
			c++;
		}
		
		
		//-------------------//----------------
		
		int j=1;
		do
		{
			System.out.println("The cvalues of J = "+j);
			j++;
		}
		while(j<11);

	}

}
